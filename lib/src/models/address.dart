class Address {
  String address;
  String number;
  String complement;
  String neighborhood;
  String city;
  String state;
  String zipCode;
  String country;
  bool isMain;
  String name;

  Address({
    required this.address,
    required this.number,
    required this.complement,
    required this.neighborhood,
    required this.city,
    required this.state,
    required this.zipCode,
    required this.country,
    required this.isMain,
    required this.name,
  });
}
